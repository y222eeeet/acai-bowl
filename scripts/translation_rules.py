# ---------------------------------------------------------------------------
# 4. 문장 구조 (Sentence)
# ---------------------------------------------------------------------------
# 마침표 뒤 띄어쓰기: ".다음" → ". 다음" (번역기가 공백 없이 이어붙이는 경우)
# 전각 마침표 "．" 도 동일 처리
SENTENCE_DOT_NO_SPACE = re.compile(r"\.([^\s])")
SENTENCE_FULLWIDTH_DOT = re.compile(r"．([^\s]?)")
# 마침표 뒤 공백 정규화: ".  " → ". "
SENTENCE_DOT_SPACE = re.compile(r"\.\s*")


def apply_style(text: str) -> str:
    """문체를 해요체로 통일."""
    s = text
    for old, new in STYLE_REPLACEMENTS:
        s = s.replace(old, new)
    return s


def apply_grammar(text: str) -> str:
    """문법·어미 보정 (것예요→거예요, 있을지도 해요 등)."""
    s = text
    for old, new in GRAMMAR_REPLACEMENTS:
        s = s.replace(old, new)
    return s


def apply_domain_glossary(text: str) -> str:
    """도메인 용어 치환. 긴 구문을 먼저 치환하기 위해 길이 내림차순 정렬."""
    s = text
    for old, new in sorted(DOMAIN_GLOSSARY, key=lambda x: -len(x[0])):
        s = s.replace(old, new)
    return s


def normalize_sentence_spacing(text: str) -> str:
    """마침표 뒤 띄어쓰기 보정. 전각 마침표(．)도 처리. 마침표 직후 비공백이면 공백 한 칸 삽입."""
    s = text
    s = SENTENCE_FULLWIDTH_DOT.sub(r". \1", s)
    s = SENTENCE_DOT_NO_SPACE.sub(r". \1", s)
    s = SENTENCE_DOT_SPACE.sub(". ", s)
    s = re.sub(r"\.(?=[^\s])", ". ", s)
    return s.strip()


def apply_all_post_process(text: str) -> str:
    """
    번역된 한국어에 규칙을 순서대로 적용.
    1. 도메인 용어 (긴 구문 우선)
    2. 문법·어미
    3. 문체 통일
    4. 문장 띄어쓰기
    (문체 통일 후 추가로 문장 끝 보정은 crawler의 _ensure_yo_style_endings에서 수행)
    """
    if not text or not text.strip():
        return text
    s = text.strip()
    s = apply_domain_glossary(s)
    s = apply_grammar(s)
    s = apply_style(s)
    s = normalize_sentence_spacing(s)
    return s
